package learn.java.udemy.javaforabsolutebeginners.unit1.fundamentals.sec3.helloworld.exercises;

public class PrintWithFormatUsingManyPrintln {
    /*
    SHOPPING LIST
        - Milk
            > Semi-skimmed
            > Medium
        - Eggs
            > Free-range
        - Bread
     */
    public static void main(String[] args) {
        System.out.println("SHOPPING LIST");
        System.out.println("\t- Milk");
        System.out.println("\t\t> Semi-skimmed");
        System.out.println("\t\t> Medium");
        System.out.println("\t- Eggs");
        System.out.println("\t\t> Free-range");
        System.out.println("\t- Bread");
    }
}
